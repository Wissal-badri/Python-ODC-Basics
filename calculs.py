import math
# Exemple d'utilisation de la fonction math.sqrt() pour calculer la racine carrée
nombre = 16
racine_carre = math.sqrt(nombre)
print(f"La racine carrée de {nombre} est {racine_carre}")

# Exemple d'utilisation de la fonction math.pow() pour calculer la puissance
base = 2
exposant = 3
resultat = math.pow(base, exposant)
print(f"{base} élevé à la puissance {exposant} est {resultat}")

# Exemple d'utilisation de la fonction math.factorial() pour calculer la factorielle
nombre = 5
factorielle = math.factorial(nombre)
print(f"La factorielle de {nombre} est {factorielle}")

# Exemple d'utilisation de la fonction math.sin() pour calculer le sinus d'un angle en radians
angle = math.pi / 2
sinus = math.sin(angle)
print(f"Le sinus de {angle} est {sinus}")

# Exemple d'utilisation de la fonction math.log() pour calculer le logarithme d'un nombre
nombre = 10
logarithme = math.log(nombre)
print(f"Le logarithme de {nombre} est {logarithme}")
